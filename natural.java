import java.io.*;
import java.util.*;
class natural
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        int k=sc.nextInt();
        int sum=0;
        for(int i=0;i<=k;i++)
        {
            sum=sum+i;
        }
        System.out.print(sum);
        
    }

}

